import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";
import type { CollectionStore } from "./collection-store.js";

const GENERATED_DIR = ".vike-content-collection";
const GENERATED_FILE = "types.d.ts";
const HEADER = "// Auto-generated by vike-content-collection — do not edit\n";

export function generateDeclarationFile(
	store: CollectionStore,
	projectRoot: string,
): void {
	const outDir = join(projectRoot, GENERATED_DIR);
	const outFile = join(outDir, GENERATED_FILE);

	mkdirSync(outDir, { recursive: true });

	const content = buildDeclarationContent(store, outDir);

	if (existsSync(outFile)) {
		const existing = readFileSync(outFile, "utf-8");
		if (existing === content) return;
	}

	writeFileSync(outFile, content);
}

const RESOLVE_CONTENT_HELPER =
	`type _ResolveContent<M> = M extends { Content: infer C } ? C\n` +
	`  : M extends { default: { Content: infer C } } ? C\n` +
	`  : M extends { default: infer D } ? D\n` +
	`  : never\n`;

export function buildDeclarationContent(
	store: CollectionStore,
	outDir: string,
): string {
	const collections = store.getAll();

	if (collections.length === 0) {
		return (
			HEADER +
			`declare module 'vike-content-collection' {\n` +
			`  interface CollectionMap {}\n` +
			`}\n`
		);
	}

	const imports: string[] = [];
	const mapEntries: string[] = [];

	for (const collection of collections) {
		const safeName = collection.name.replace(/[^a-zA-Z0-9]/g, "_") || "_root";

		const configRelative = relative(outDir, collection.configPath)
			.replace(/\\/g, "/")
			.replace(/\.(ts|js|mts|mjs)$/, "");

		imports.push(
			`import type * as _${safeName} from '${configRelative}'`,
		);
		mapEntries.push(
			`    '${collection.name}': z.infer<_ResolveContent<typeof _${safeName}>>`,
		);
	}

	return (
		HEADER +
		`import type { z } from 'zod'\n` +
		imports.join("\n") +
		"\n\n" +
		RESOLVE_CONTENT_HELPER +
		"\n" +
		`declare module 'vike-content-collection' {\n` +
		`  interface CollectionMap {\n` +
		mapEntries.join("\n") +
		"\n" +
		`  }\n` +
		`}\n`
	);
}
